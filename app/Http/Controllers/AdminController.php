 public function adminLoginCheck(Request $request)
    {
        $email=$request->email;
        $$password=$request->password;

        $result=DB::table('admins')
        ->where('email', $email)
        ->where('password', md5($password))
        ->first();

        if($result){
         return redirect()->route('backend-dashboard');
    }else{
         return Redirect::to('/');
    }

        return view('admin.dashboard');
    }
